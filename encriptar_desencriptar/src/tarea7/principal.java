    private void jButton_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_aceptarActionPerformed
        if (jTextField_usuario.getText().isEmpty() || jTextField_pass.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Se debe introducir usuario y contrase침a", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                usuario = jTextField_usuario.getText().toString();
                password = jTextField_pass.getText().toString();
                
                //generamos una clave de 128 bits con semilla del usuario y la contrase침a introducida
                KeyGenerator kg = KeyGenerator.getInstance("AES");
                String semilla = usuario+password;
                
                SecureRandom sr = new SecureRandom();
                // Establecemos la semilla
                sr.setSeed(semilla.getBytes());
                //asignamos la longitud
                kg.init(128, sr);
                //genermos la clave
                clave = kg.generateKey();
                
                //abrimos un di치logo para introducir el texto a encriptar
                DialogoEncriptar dialog = new DialogoEncriptar(new javax.swing.JFrame(), true, clave);
                dialog.setVisible(true);
                
            } catch (NoSuchAlgorithmException ex) {
                ex.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton_aceptarActionPerformed

